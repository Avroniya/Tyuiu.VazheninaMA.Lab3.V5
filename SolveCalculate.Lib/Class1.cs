using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SolveCalculate.Lib
{
    public static class Calculate
    {
        public static double CalculateSumma(double a, float b)
        {
            return a + b;
        }
        public static double CalculateDifference(float a, float b)
        {
            return a - b;
        }
        public static double CalculateMultiplication(float a, float b)
        {
            return a * b;
        }
        public static float CalculateQuotient(float a, float b)
        {
            return a / b;
        }
    }

}
